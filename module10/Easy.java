package com.varaxina.module10;

/*
1. Полиморфизм — это способ, когда один объект или метод может работать
по-разному в зависимости от того, с каким классом он используется.

2. Абстрактный класс может содержать поля и реализованные методы, интерфейс — только методы.
Интерфейс используется, когда нужно задать общее поведение для разных классов,
а абстрактный класс — когда есть общая реализация и состояние, которое можно наследовать.

3. Создать объект абстрактного класса нельзя.
Создать объект интерфейса тоже нельзя. Можно только через реализацию.

4. Анонимные классы — это классы без имени, которые создаются прямо в месте использования.
Применяются, чтобы быстро реализовать интерфейс или наследника без создания отдельного файла.

5. Такие интерфейсы не содержат методов, а служат меткой, чтобы сообщить системе или другим классам,
что объект обладает каким-то свойством.

6. Обобщённые типы позволяют создавать классы, интерфейсы и методы, которые могут работать
с разными типами данных, не теряя безопасности типов.

7. Сверху (extends): указывает, что тип должен быть наследником определённого класса или интерфейса.
Снизу (super): указывает, что тип должен быть родителем определённого класса.
 */